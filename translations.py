def get_text(lang: str, key: str, **kwargs) -> str:
    """Get translated text for a given language and key."""
    lang = lang or "en"
    if lang not in TRANSLATIONS:
        lang = "en"
    
    text = TRANSLATIONS[lang].get(key, TRANSLATIONS["en"].get(key, key))
    
    if kwargs:
        try:
            text = text.format(**kwargs)
        except KeyError:
            pass
    
    return text
